package controlflow;

public class IfElseIfStatement {
    public static void main(String[] args) {
        String city = "Tampa";

        if (city == "New York") {
            System.out.println("City is New York");
        }else if (city == "Naples") {
            System.out.println("City is Naples");
        }else if (city == "Manville") {
            System.out.println("City is Manville");
        }else{
            System.out.println("City is Tampa");
        }
    }
    
}
