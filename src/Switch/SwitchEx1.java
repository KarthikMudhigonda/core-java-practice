package Switch;

public class SwitchEx1 {
    public static void main(String[] args) {
        int i = 20;
        switch(i){
        case 10: System.out.println(10);
        case 20: System.out.println(20);
        case 30: System.out.println(30);
        default: System.out.println("not in 10,20,30"); 
    }
}
}
