package Operaters;

public class LeftShift {
    public static void main(String[] args){
        System.out.println(10<<2); //The Java left shift operator << is used to shift all of the bits in a value to the left side of a specified number of times.
        System.out.println(11<<3); //The << operator shifts the bits of a number to the left by a specified number of places
    }
    
}
